"""
Los operadores aritmeticos son todos los relacionados
con calculos matematicos, los que esta habilitados por
defecto en Python son:

- suma
- resta
- multiplicación
- división
- división entera
- modulo
- potencia
"""
# declaramos dos variables
a = 10
b = 5

# obtenemos los resultados de cada operando aritmetico
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
division_entera = a // b
modulo = a % b
potencia = a ** b # 10 expo 5

# imprimimos los resultados
print('X: ', a, ' - Y: ', b)
print('suma: ', suma)
print('resta: ', resta)
print('multiplicacion: ', multiplicacion)
print('division: ', division)
print('division_entera: ', division_entera)
print('modulo: ', modulo)
print('potencia: ', potencia)