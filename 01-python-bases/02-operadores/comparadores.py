"""
Los comapradores son las operacipones que retornan
valores booleanos, y estan dados por:

- menor que
- menor mayor que
- mayor que
- mayor menor que
- diferente de
- igual que

"""

a = 10
b = 10


print(a > b) # mayor
print(a >= b) # mayor o igual
print(a < b) # menor
print(a <= b) # menor o igual
print(a == b) # igual
print(a != b) # diferente
